def bboxToBytes(bbox_array):
  """
  Params:
          bbox_array: Numpy array (pixels) containing rectangle to overlay on video stream.
  Returns:
        bytes: Base64 image byte string
  """

  bboxPIL = PIL.Image.fromarray(bbox_array, "RGBA")
  iobuf = io.BytesIO()
  bboxPIL.save(iobuf, format='png')
  bboxBytes = "data:image/png;base64,{}".format((str(b64encode(iobuf.getvalue()), 'utf-8')))

  return bboxBytes

