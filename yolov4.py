def jsToImage(js_reply):
    """
    Params:
            js_reply: JavaScript object containing image from webcam
    Returns:
            img: OpenCV BGR image
    """

    base64_data = js_reply.split(',')[1]

#   imageBytes = b64decode(js_reply.split(','[1]))
    imageBytes = b64decode(base64_data)
    jpgAsNp = np.frombuffer(imageBytes, dtype=np.uint8)
    img = cv2.imdecode(jpgAsNp, flags=1)

    return img


